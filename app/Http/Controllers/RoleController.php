    {

        $request->validate([
            'user_id' => 'required',
            'role_id' => 'required|integer',
        ]);

        $user = User::findOrFail($request->user_id);


        $user->role_id = $request->role_id;
        $user->save();

        return redirect()->route('adminDashboard')->with('success', 'User role updated successfully.');
    }
